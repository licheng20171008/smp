package com.yx.ssm.po;

public class DetailnormExt extends Detailnorm {

	private String firstnormName;
	
	private String secondnormName;
	
	private String dpName;

	public String getFirstnormName() {
		return firstnormName;
	}

	public void setFirstnormName(String firstnormName) {
		this.firstnormName = firstnormName;
	}

	public String getSecondnormName() {
		return secondnormName;
	}

	public void setSecondnormName(String secondnormName) {
		this.secondnormName = secondnormName;
	}

	public String getDpName() {
		return dpName;
	}

	public void setDpName(String dpName) {
		this.dpName = dpName;
	}
}
